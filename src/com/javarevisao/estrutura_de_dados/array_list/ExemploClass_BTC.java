package com.javarevisao.estrutura_de_dados.array_list;

public class ExemploClass_BTC {
    float numero;
    String nome;

    public ExemploClass_BTC(float numero, String nome) {
        this.numero = numero;
        this.nome = nome;
    }

    public float getNumero() {
        return numero;
    }
    public String getNome() {
        return nome;
    }

}
